# 다음과 같이 리스트를 만들고 
# for문으로 요소를 하나씩 출력하세요
mixed=[1, "apple",3.14,True]

# for 변수 in 자료구조(리스트,튜플등)
for m in mixed:
    print(m)

    # 다음과 같이 리스트를 만들고
    # for문으로 요소를 하나씩 출력하세요
    fruits = ["apple","banana","cherry"]

    for f in fruits:
        print(f)

# 다음과 같이 딕셔너리를 만들고
# for문으로 key를 하나씩 출력하세요
students={"이름":"둘리", "나이":20, "주소":"인천"}
for key in students:
    print(key)   #이름 나이 주소


# 다음과 같이 딕셔너리를 만들고
# for문으로 valure를 하나씩 출력하세요
coffee_menu={"아메리카노": "2500원" ,"라떼":"3000원", "케이크":"4500원"}
#for 변수 in iter
for value in coffee_menu.values():
    print(value)





